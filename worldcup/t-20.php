<h1>T20 World Cup Winners</h1>

<p>The winner of the first T20 cricket world cup was India. The ICC T20 World Cup takes place once every two years.
     However, due to the COVID-19 pandemic and some planning changes made by the International Cricket Council (ICC),
      there has not been a single T20 Cricket World Cup in the last five years. The last T20 World Cup was played in 2016.
       Now, ICC will conduct two T20 World Cups in the upcoming two years (2021 and 2022). 
</p>

