<h2>Former NZ captain Barry Sinclair passes away aged 85</h2>

<section class="cb-nws-dtl-itms" itemprop="articleBody"> <p class="cb-nws-para"> Barry Sinclair, the former New Zealand cricketer, has passed away at the age of 85.
     Sinclair, who captained New Zealand in three Test matches, was the third player from the country to score 1000 runs after Bert Sutcliffe and John R Reid. </p> 
</section>

     <section class="cb-nws-dtl-itms" itemprop="articleBody"> <p class="cb-nws-para"> Sinclair played 21 Tests between 1963 and 1968, scoring 1148 runs. 
        He also struck three centuries - one apiece against South Africa, Pakistan and England. Sinclair featured in a total of 118 first-class games, 
        scoring 6114 runs with six hundreds and 38 fifties. </p> </section>

        <section class="cb-nws-dtl-itms" itemprop="articleBody"> <p class="cb-nws-para"> Sinclair was involved with the New Zealand Cricket Players Association, 
            becoming the inaugural patron in 2008-09. </p> 
        </section>

<h2>Zimbabwe trounce Singapore in tournament opener, USA ease past Jersey</h2>

    <p>Hosts Zimbabwe smashed Singapore by a record 111 runs in their opening match at the T20 World Cup Global Qualifier B at Queens Club, Bulawayo.
     Sikandar Raza and Sean Williams struck rapid half-centuries as Zimbabwe racked up up 236/5 before restricting Singapore to 127/7 to secure two points
      and a healthy net run rate to go top of Group A.
    </p>


    <p>Meanwhile, at Bulawayo Athletic Club, the USA recorded a convincing win over Jersey, though a potential injury to star quick Ali Khan might yet dampen their celebrations.
     Teenaged Jersey bat Asa Tribe and Ben Ward lifted the Channel Islanders to 154/5 after a shaky start to the innings but the total proved insufficient in the face of an in-form Steven Taylor,
     who made full use of two early reprieves to see his side comfortably home, sending the final ball high over long on to seal the win - and a maiden T20I century - with two overs to spare.
    </p>

   <p>After losing the toss and being put in to bat, Zimbabwe captain Craig Ervine got his side off to a rollicking start,
     as together with Regis Chakabva he took the hosts past fifty in the space of just 21 balls. The partnership ended at 51,
      however, and Ervine in part to blame for luring his partner down the track with a stuttered start but Janak Prakash deserved 
      most of the credit for a calm pick up in his follow-through and direct hit to run out Chakabva for a 10 ball 23.
    </p>

<h2>Chamdimal piles on the runs; Sri Lanka cement strong position</h2>

<p>Sri Lanka built on their overnight lead steadily through the morning on the fourth day with Dinesh Chandimal getting to an unbeaten 153.
     Chandimal also added 68 with Ramesh Mendis for the seventh wicket that frustrated Australia for a large part of the fourth morning.
</p>

<p>Both Chandimal and Mendis were proactive in their approach to counter Nathan Lyon's offspin. 
    While the former brought out a string of sweep shots, the latter also put the spinner off by dancing down the track and lofting him.
     As a result, Australia had little to no success through the first hour and Sri Lanka stretched their lead swiftly
</p>

<p>With no luck going their way, Australia only managed to get back into the game when the third new ball was taken. 
    Ramesh was yorked by Mitchell Starc and trapped plumb in front. This came in the middle of a flurry of opportunities for Australia.
     A couple of overs before, Mitchell Swepson had an LBW turned down, only for the replays to show three reds. With no review left, 
     Australia were left ruing a missed chance when Starc nearly struck twice. 
     After bagging Ramesh, he also had a caught behind appeal against Maheesh Theekshana upheld onfield but it was overturned on review.
</p>

