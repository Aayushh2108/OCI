  <h1>Icc Champions Trophy</h1>
  <p>The ICC Champions Trophy is a One Day International (ODI) cricket tournament organised by the International Cricket Council (ICC). 
    It was inaugurated as the ICC Knock Out Tournament in 1998 and has been played approximately every two years since. 
    Its name was changed to the Champions Trophy in 2002. 
    Since 2009, the tournament has only involved the eight highest-ranked ODI teams as of six months prior to the tournament.
     The 8th edition of the ICC Champions Trophy is scheduled to be held in England and Wales between June 1-18, 2017.
  </p>

