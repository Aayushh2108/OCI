    <h1>2013 ICC Champions Trophy</h1>
    <p>Won by  India <br>

        Main article: 2013 ICC Champions TrophyEngland and Wales hosted the 2013 Champions Trophy.
         England became the only country to host the Champions Trophy twice. Australia failed to win a single game in their group, and were knocked out along with New Zealand in Group A.
          Pakistan lost all three games in Group B and were knocked out along with West Indies. England and Sri Lanka from Group A,
           and India and South Africa from Group B, made it to the semi-finals.

India and England won their respective games against Sri Lanka and South Africa comprehensively and the final between the two took place on 23 June 2013.
 India beat England by 5 runs at Edgbaston, winning their second title, although their first title, in 2002, was shared with Sri Lanka due to the final being washed out.
  Ravindra Jadeja was adjudged man of the match and he also received the "Golden Ball" for taking the most wickets in the tournament. 
  Shikhar Dhawan received the "Golden Bat" for scoring the most runs in the series and was also adjudged the Man of the Series for his consistent outstanding performances. 
MS Dhoni became the first captain in history to win all three major ICC trophies – World Cup in 2011, World T20 in 2007 and this edition of the Champions Trophy.</p>


<h1>2017 ICC Champions Trophy</h1>

<p>Won by  Pakistan <br>

Main article: 2017 ICC Champions Trophy
In the lead-up to the 2013 tournament, the ICC announced that the 2013 Champions Trophy was to be the last, with its place in the cricketing calendar to be taken by a new ICC World Test Championship.
 However, in January 2014, that decision was reversed, due to the massive success of the 2013 edition, with the ICC confirming that the 2017 Champions Trophy tournament would take place and the proposed Test Championship was cancelled.
  England and Wales hosted the 2017 ICC Champions Trophy. 
  England became the only country to host the Champions Trophy thrice, and England and Wales became the only countries to host the ICC Champions Trophy consecutively, also hosting the 2013 edition. 
  Bangladesh replaced the West Indies, who finished outside the top eight in ninth position, in the ICC ODI Team Rankings on the cut-off date.
   Bangladesh returned to the ICC Champions Trophy for the first time since 2006, and, for the first time, the West Indies failed to qualify.

Security around the tournament was increased following the Ariana Grande concert attack in Manchester, just before the start of the competition.
 The International Cricket Council (ICC) announced that they would review security concerns. The 15 games played in the tournament were held across three venues – The Oval in London, Edgbaston Cricket Ground in Birmingham and Sophia Gardens in Cardiff. 
 India did not announce their squad by the 25 April deadline due to what it described as "operational" reasons, although this was widely seen as a protest by the Board of Control for Cricket in India (BCCI) in an ongoing disagreement with the ICC over finance and governance.
  After interference from senior officials, the Indian squad was finally named on 8 May 2017. Pakistan's Shoaib Malik played in his sixth consecutive Champions Trophy.</p>


  <h1>2021 ICC Champions Trophy</h1>

  <p>In the lead-up to the 2017 tournament, the ICC had proposed starting an ODI League in 2019, which will most likely lead to the Champions Trophy getting scrapped.
   Following the 2017 Champions Trophy, David Richardson (the ICC CEO) stated that the future status of the Champions Trophy was undecided, with both a possible Test league and an additional World T20 putting additional pressure of fixtures.
   In December 2017, the ICC's Future Tours Programme listed the 2021 edition taking place in India. However, in April 2018, the ICC announced that the tournament was scrapped, with the possibility of a T20 World Cup tournament replacing it. 2021 ICC T20 World Cup was originally due to be hosted in India, however, it was moved to UAE due to the COVID-19 pandemic.</p>

